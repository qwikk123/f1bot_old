package org.f1bot;

public class Constructor {
    int pos;
    String name;
    String nationality;
    double points;
    int wins;

    public Constructor(int pos, String name, String nationality, double points, int wins) {
        this.pos = pos;
        this.name = name;
        this.nationality = nationality;
        this.points = points;
        this.wins = wins;
    }
}
